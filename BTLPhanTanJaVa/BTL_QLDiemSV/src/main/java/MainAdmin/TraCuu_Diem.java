    private void LoadDataTable(List<DiemThi> score ){
        try {
           for(DiemThi diem : score){

                    MonHocDao mhd=new MonHocDao();
                    MonHoc monhoc=mhd.FindMH(diem.getMaMH(), datasql);
            
               tblTableModell.addRow(new Object[]{
                   monhoc.getTenMH(), diem.getDiemGK(), diem.getDiemCK(), diem.getDiemTK()});
                 
                }
        } catch (Exception e) {
        }
    }
