    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        initTable();
        String id1 = txtParam.getText();
        String Hoten=txt_searchTen.getText();
        String id="";
        if (id1.equals("") && (Hoten.equals("")))
        {
            MessageHelper.showMesage(parentform,"Vui lòng điền thông tin tra cứu","Thông Báo");
            return;
        }
        try {
            Socket client = new Socket("localhost", 9999);
            DataOutputStream DOS = new DataOutputStream(client.getOutputStream());
            ObjectOutputStream OOS = new ObjectOutputStream(client.getOutputStream());
            ObjectInputStream OIS = new ObjectInputStream(client.getInputStream());
            if (!id1.equals("")) 
            {
                id=MaHoaDes.Encrypt(id1, id1);
            }
            else id=Hoten;  
            DOS.writeInt(6);
            System.out.println(datasql.getPort());
            OOS.writeObject(datasql);
            DOS.writeUTF(id);
            Thread.sleep(100);
            boolean check = (boolean) OIS.readBoolean();
            if (check != false) {
                SinhVien sv = (SinhVien) OIS.readObject();
                Thread.sleep(100);
                List<DiemThi> score = (List<DiemThi>) OIS.readObject();
                txtName.setText(sv.getHoTen());
                txtEmail.setText(MaHoaDes.Decrypt(sv.getEmail(), id));
                txtId.setText(MaHoaDes.Decrypt(sv.getMaSV(),id));
                txtPhone.setText(MaHoaDes.Decrypt(sv.getSDT(),id));
                LoadDataTable(score);
            } else {
                SinhVien sv = (SinhVien) OIS.readObject();
                MessageHelper.showMesage(this,"Thông tin không tồn tại \n" + "Vui lòng thử lại", "Thông Báo");
                    
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed


